          <h2>Man About Town <br>by Christopher Keizur for Gresham Outlook</h2>
          <br>
          <p>One afternoon Gresham Fire Chief Greg Matthews was cruising through the city when he spotted Todd Kirnan — commonly considered the community's kindest resident.</p>

          <p>Kirnan told his friend he was hungry, so Matthews treated him to lunch at a favorite local spot of theirs, Dea's In & Out. Kirnan's appetite is as large as his heart, apparently, as he ordered a bacon cheeseburger with chili-cheese curly fries and a large shake.</p>

          <p>But right as their food arrived, Kirnan's cellphone rang. It was one of his many clients in downtown Gresham who was hoping Kirnan could pick up a drink for them at Café Delirium. Without hesitation, Kirnan jumped up and headed for the door — only agreeing to stay and enjoy the meal after Matthews called back and confirmed it was all right for the client to wait for the coffee.</p>

          <p>"Todd, you are the man," Matthews said. "Thank you for everything you do for this community."</p>

          <p>Saturday afternoon, Sept. 22, Kirnan brought the community together in a way that only someone fondly known as "Mr. Gresham" could. Thanks to donations large and small from many people across the city, a statue of Kirnan was unveiled during a community celebration in downtown.</p>

          <img src="img/toddWithStatue.jpg" alt="Todd Kirnan next to the Mr. Gresham Bronze." class="center"/>
          Outlook Photo: Christopher Keizur

          <br>
          <br>
          <p>The life-sized bronze statue, created by Cascade Locks-based artist Heather Greene, shows Kirnan with his patented smile delivering food in a Clyde Drexler Portland Trail Blazers jersey. The creation is found at the corner of Main Avenue and Third Street in the heart of downtown Gresham.</p>

          <p>"Thank you for everyone being here for me," Kirnan said. "I love you guys."</p>

          <p>Kirnan was diagnosed with autism shortly after birth and suffered through a rough childhood, including abuse and foster care. He never let that challenging start to life slow him down, however, and today is the smiling face of downtown Gresham.</p>

          <p>He spends his days seeing if there is anything he can do to help others, be it a cheerful smile, warm greeting or lending a helping hand to local business owners. For the past 15 years, he has run an informal business where he delivers food and coffee, takes out recyclables, mails letters, fetches stamps and does whatever else is asked of him.</p>

          <p>"We love Todd Kirnan, and we love what Todd Kirnan says about who Gresham is as a community," said Gresham Mayor Shane Bemis, who has been friends with Kirnan since they attended Gresham High School together.</p>

          <p>Gresham Outdoor Public Art (GOPA) led fundraising efforts to make the statue a reality, while the city of Gresham helped organize the event. Funding for the statue is still ongoing, as no public dollars are being used.</p>

          <p>"Todd is a special person who reminds us of our humanity," said Judy Han, chairwoman of GOPA. "We are honoring someone who has made an impact on our community."</p>

          <p>A mini-parade kicked things off on Saturday as Kirnan was escorted to the statue in a local resident's "Batmobile" — in honor Kirnan's favorite superhero — surrounded by cheerleaders, police officers and firefighters — cheered on by almost 600 people lining the streets.</p>

          <p>There was live music and dancing, with Kirnan joining SALT Academy for a routine. Jazzy Bagels donated free coffee and bagels, Sugar Cubed Cakes brought cupcakes, and those who went got to explore more than 20 businesses offering "Todd Specials." The local celebration even caught the eye of national media, as CBS National Morning News' Steve Hartman was in Gresham as part of his 'On The Road' series.</p>

          <p>And though Drexler, the former basketball star and Kirnan's favorite player, couldn't make it because of business obligations in Europe, he did send a letter that Bemis read.</p>

          <p>"I have heard about what you do for the city," Drexler wrote. "Thank you for your service. I can't wait to see the statue and you next time I am in Gresham."</p>

          <p>The mayor concluded his speech by declaring Sept. 26, 2018, "Todd Kirnan Day."</p>

          <p>"My dad is looking at me from heaven today," Kirnan said of his father and best friend George, who passed away in 2017.</p>

          <h3>Bronzing Todd</h3>

          <p>Sculpting Kirnan was a fun process for Greene, as it was the first time she has captured the likeness of a living person.</p>

          <p>Usually she has to depict the spirit of her subject via grainy black-and-white photographs. This time, however, she was able to interact with Kirnan while crafting the piece. That included measurements and having him pose every which way.</p>

          <p>"He is so fun to sculpt," Greene said. "Todd is the best model I've ever had."</p>

          <p>The first step in bronzing Kirnan was a photo shoot to capture all of the images Greene later used for her work. The pose was never in question, as showing Kirnan helping others by delivering food depicts the spirit he brings to the city.</p>

          <p>When sculpting, Greene always starts with the face, as it's the focal point of the entire piece.</p>

          <p>"Todd has such a unique face, so it's tricky to get the look," she said.</p>

          <p>As Greene worked, she would constantly go back and tweak different aspects of the sculpture. The difficulty is that a statue is a three-dimensional object.</p>

          <p>"A statue is less about form than it is about light and shadow," she said. "Clay is different than bronze, because you get an organic feel rather than metal. So, you have to think about how it will all look when its finished."</p>

          <p>Once the piece was completed in clay, and received the thumbs-up from Kirnan, her whole team put it through the 21-step casting process to immortalize him in bronze. The process took only three months, which is an incredibly short time for this type of art. In addition to Greene, Rich Greene, Don Berry, Brenda Stipan and Mauro Alvarez all worked to finish the sculpture in time for the party. Alvarez even canceled a huge family vacation to finish the piece.</p>

          <p>"It was all worth it to see Todd with the piece," Greene said.</p>

