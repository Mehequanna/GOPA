            <h2>Painting big in Rockwood
                <br>by Christopher Keizur for Gresham Outlook</h2>
            <br>
            <p><strong>Graffiti artist Rico Alvarez to celebrate culture, produce with downtown Rockwood murals</strong></p>

            <img src="img/marketplace/article1.jpg" alt="Rico Alvarez standing in front his graffiti art" class="center"/>
            PMG PHOTO: CHRISTOPHER KEIZUR - Greshams Rico Alvarez painted a sweeping, out-of-this-world graffiti piece with two friends in the alleyway beside Cheap Charlies in downtown.
            <br>
            <br>

            <p>
                A Gresham artist got his start scrawling his name in graffiti.
            </p>
            <p>
                That wasn't a unique origin story for Rico Alvarez, who grew up in California and Central Mexico.
                As a 13-year-old the allure of spray paint and making a tangible mark on the world made more sense
                than falling into the dangers and violence that preyed on youngsters in his community.
            </p>
            <p>
                Instead of joining a gang, Alvarez became connected with a cohort of friends who all shared a love
                of art. In the beginning that meant quickly tagging different places in his neighborhood and trying
                to elevate his name recognition.
            </p>
            <p>
                "Graffiti provided an outlet to be creative, and it brought all of us from different backgrounds
                together," Alvarez said. "If I hadn't spent the time learning how to scribble my name in weird ways,
                I wouldn't be painting these larger pieces now."
            </p>
            <p>
                After several police departments politely asked him to stop, Alvarez joked, he refocused on want he
                wanted to create and why he was so drawn to graffiti. For him, it was about being able to paint big —
                using bold colors, intricate designs and blending words and images in unique ways.
            </p>
            <p>
                "Art is an outlet to be creative that can resonate on so many different levels," he said.
            </p>

            <img src="img/marketplace/article2.jpg" alt="Bright and colorful mushroom" class="center"/>
            COURTESY PHOTO: RICO.NOT.SO.SUAVE ON INSTAGRAM - Rico Alvarez loves to work with bright greens and purples in his art.
            <br>
            <br>

            <p>
                Those bold creations led Alvarez down a path to being commissioned to paint for a new development in
                the heart of Rockwood, celebrating the food culture in the neighborhood. Gresham Outdoor Public Art
                is working alongside Alvarez to create a pair of murals inside the soon-to-be-completed Rockwood
                Market Hall, which is part of the Downtown Rockwood development.
            </p>
            <p>
                Alvarez, 39, was happy to jump on board because the new Market Hall is eliminating a former food
                desert and celebrating the uniqueness of the community.
            </p>
            <p>
                "The space is really awesome, it's going to feel like a farmers market," Alvarez said. "All that's
                missing right now is the people and the food."
            </p>
            <p>
                It's difficult to pin down who Alvarez is as an artist.
            </p>
            <p>
                Some would call him a muralist who works in both abstract and surrealism. He paints and draws, both
                in the real world and digitally. He enjoys creating artistic stickers, sculpting, and is passionate
                about typography and creating new ways to write words — a throwback to his preteen days scrambling
                across the city with a spray can in hand.
            </p>
            <p>
                Much of his work can be found under the nom de plume Rico DeGallo.
            </p>
            <p>
                "I wanted an artist name that didn't have anything to do with who I am personally, but was still
                close to my real name," he said.
            </p>
            <p>
                He's working on creating a website to celebrate and sell his art. Meanwhile, his work can be found
                on Instagram @rico.not.so.suave, and at PDXchange, 3916 N. Mississippi Ave., often sells his pieces.
                But right now the best way to discover Alvarez's work is to get out into the community.
            </p>
            <p>
                "Being an artist, it's tough," Alvarez said. "Look at my shoes — they have holes and are covered in paint."
            </p>

            <p><strong>Catholicism, Disney and slime</strong></p>
            <img src="img/marketplace/article3.jpg" alt="Picture of graffiti art on Cheap Charlies" class="center"/>
            COURTESY PHOTO: RICO.NOT.SO.SUAVE ON INSTAGRAM - Rico Alvarez loves to work with bright greens and purples in his art.
            <br>
            <br>

            <p>
                Three main influences can be seen in Alvarez's art — Chicano culture, Catholicism and Disney.
            </p>
            <p>
                Growing up he was surrounded by vibrant colors and imagery being raised in a Catholic home. Alvarez
                was drawn to those bold statements in his own work.
            </p>
            <p>
                As for the House of Mouse, one movie that really stuck with him was Sleeping Beauty.
            </p>
            <p>
                "I was blown away during the scene where Maleficent transformed into a dragon," he said. "I didn't
                think you could make greens pop like that."
            </p>
            <p>
                In his own work Alvarez loves to incorporate greens and purples, violets and pinks. Like how the
                Disney animators were able to create something beautiful out of a villain's dark entrance to the film,
                Alvarez tries to juxtapose dark subject matters with bright colors.
            </p>
            <p>
                He also is inspired by nature. Alvarez maintains a saltwater aquarium that he will often stare into,
                looking at the darting fish and bulbous coral. His paintings introduce trees, slime molds, amphibians,
                all sorts of things that may change how he curves a letter or adds a pattern onto a character.
            </p>
            <p>
                "My advice to people who want to get into any kind of art is when you are inspired, start to draw
                right away," Alvarez said. "Don't go buy a kit, or set yourself up to paint someday, start right
                away on that napkin."
            </p>
            <p>
                A great example of Alvarez's work can be found on the wall in an alley way next to Gresham's Cheap
                Charlie's Beer & Wine Superstore, 79 N.E. Roberts Ave.
            </p>
            <p>
                He and some friends had visions for what they could create on that wall, which was a graffiti artist's
                dream canvass with a large, uninterrupted surface that was prominent within the community and easy to reach.
            </p>
            <p>
                "I called up some buddies and asked if they wanted to paint the wall with me," said Alvarez, who got
                the green light from the owner.
            </p>

            <img src="img/marketplace/article4.jpg" alt="Skeleton looking lovingly at cactus" class="center"/>
            COURTESY PHOTO: RICO.NOT.SO.SUAVE ON INSTAGRAM - Rico Alvarez loves to work with bright greens and purples in his art.
            <br>
            <br>

            <p>
                Alvarez and two of his artist friends, who wished to remain unnamed, painted the 80-foot-long wall
                at no charge. For them it wasn't about the money, but about expressing themselves artistically and
                perhaps inspiring others in the community to dive into art.
            </p>
            <p>
                The graffiti piece in the Cheap Charlies alleyway, which was completed earlier this year, transports
                people into outer space, with colorful galaxies, spaceships, aliens and intricate patterns. All three
                artists added their own flair to the piece, and it turned into a fun way to spend COVID afternoons.
            </p>
            <p>
                "We spent a long time working on that piece, it was something we didn't want to let go," Alvarez
                said with a laugh. "At one point it was just a hangout spot for us to eat pizza."
            </p>

            <p><strong>Disappearing produce</strong></p>
            <img src="img/marketplace/article5.jpg" alt="Aerial photo of Rockwood Market Hall" class="center"/>
            COURTESY PHOTO: CITY OF GRESHAM - The Downtown Rockwood Market Hall will celebrate the culture and diversity
            of the neighborhood with amazing food.
            <br>
            <br>

            <p>
                When Alvarez was first commissioned to paint the murals at the Rockwood Market Hall, the first thing
                he had to do was learn how to draw produce.
            </p>
            <p>
                "One of the reasons I failed art class back in the day was because I refused to draw still life,"
                he said with a laugh. "So I had to learn those skills."
            </p>
            <p>
                He is painting two pieces, each 4-by-18 feet, that will hang above the interior entrances to the
                market. One will feature vegetables, the other fruit.
            </p>
            <p>
                "The art will be something bright for the space," Alvarez said. "Produce is so colorful, and the
                pieces will show the different cultures and foods in the neighborhood."
            </p>
            <p>
                The market hall is perhaps the centerpiece of the Downtown Rockwood development, located between
                Southeast Stark Street, Southeast 185th Avenue and East Burnside Street, which will have a central
                square with a public plaza and play structures for kids, an innovation hub with services for locals,
                retail stores, apartments, and the market hall.
            </p>
            <p>
                The 5.5-acre plot was initially purchased by the Gresham Development Commission in 2005 with funds
                from the city's urban renewal district. It broke ground in the summer of 2019.
            </p>
            <p>
                The Market Hall, where Alvarez will have his murals, will be a curated final mix of micro-restaurants,
                ethnic markets and fresh food stalls, with communal open seating, office space, food storage and a
                commissary kitchen.
            </p>
            <p>
                "The reason we are here is because we think we can reflect the food of Rockwood," said Roy Kim,
                developer of the Rockwood Rising project. "It's very rare to be able to put this all under one roof."
            </p>

            <img src="img/marketplace/article6.jpg" alt="Photo of Rico Alvarez" class="center"/>
            PMG PHOTO: CHRISTOPHER KEIZUR - Artist Rico Alvarez has been commissioned to create a pair of murals for
            the Downtown Rockwood Market Hall.
            <br>
            <br>

            <p>
                The Market Hall is shooting for a January 2022 opening, and Alvarez has begun working on his murals.
                He has a rough outline for how it will all look, and is collecting materials to assemble a pair of
                wooden panels as his canvas.

            </p>
            <p>
                True to his graffiti background, where your work is constantly being painted over and disappearing,
                Alvarez sees the new murals as ephemeral. By painting on those panels, it will allow the future
                operators of the Market Hall to easily take them down and put up new artwork.

            </p>
            <p>
                "It is amazing to be a part of this project, but I would like for the pieces to be taken down in the
                future," he said. "Someone else's work could be put up there to celebrate even more artists in this
                community."
            </p>

