            <p>A group of eighth graders got the surreal experience of painting their peers, and in some cases themselves, during a project that celebrates generational learning through vibrant, colorful art.</p>
            
            <p>The project is going up on a prominent outside wall at Clear Creek Middle School, where hundreds of students pass by daily after being dropped off by parents.</p>
            
            <p>The art features many different people from various backgrounds.</p>
            
            <p>Shane is one of the front-and-center models in the mural. The painting showcases his soccer skills, a sport he fell in love with after friends prompted him to join on the pitch three years ago.</p>
            
            <p>“It feels awesome to work on the mural, I'm proud of what we have done,” Shane said. “Every day progress is made.”</p>
            
            <p>Luna is another prominent figure in the mural. It shows her playing the cello, one of her many passions. In it her hair is done up and she has colorful eye shadow — because when the source photo was taken she was heading straight to a Ballet Folklorico performance.</p>
            
            <p>“I like music because it's so fun to hear the beats — it is the same thing with dancing,” she said. “It has been fun painting myself, though a little weird seeing myself up there.”</p>
            
            <p>Berenice is in the painting cooking chicharróns with her grandmother.</p>
            
            <p>“It was nice being with her — you learn so much about (people) when you spend time with them,” she said. “It is nice to think back to that moment while I paint.”</p>
            
            <p>Gavin got the strange experience of painting the back of his head while working on the mural. His portion features his father teaching him to play the guitar.</p>
            
            <p>“It was nice to share something with my dad,” he said.</p>
            
            <p>He comes from a musical family — Gavin sings as part of the Clear Creek choir — and pointed out some of the similarities between the art forms.</p>
            
            <p>“I like being able to zone out and create something, whether it's playing music or painting,” he said. “It's crazy remembering when this was just a boring wall.”</p>

            <p><strong>
              Mural and mustaches
            </strong></p>

            <p>Lindsay King, a social studies teacher at Clear Creek, spurred the 43-foot-wide mural.</p>

            <p>The inspiration for the project came from “My Name is Lamoosh,” a book written by Linda Meanus. It talks about growing up in the Warm Springs Tribe and what was learned from elders.</p>
            
            <p>That prompted a project for the kids — what have they learned from the elders in their lives. That was combined into the design of the mural. Thus actual students emblazoned through the art.</p>
            
            <p>“This mural celebrates all the things people bring to this community, and the many ways things are shared with younger generations,” King said.</p>
            
            <p>“There is so much anxiety and uncertainty right now, and so many negative reports about education, I wanted to present a counter story,” King added. “I'm on a mission to celebrate and bring more student color and creativity to this school.”</p>
            
            <p>In total 150 students have worked on the mural. That includes Payton and Abby, who have loved bolstering an interest in art while celebrating their friends.</p>
            
            <p>“We've been able to watch something bigger than us come together,” Payton said. “We are leaving an imprint on the school.”</p>
            
            <p>“It has been fun, I have painted before with my grandmother but nothing this big,” Abby said. “And we can also tease people and say we are going to paint a mustache on them.”</p>

            <p><strong>
              Community funding
            </strong></p>

            <p>There is a kinetic vibrancy to the mural, which flows from vignette to vignette. Assisting in the project has been artist Sarah Farahat.</p>

            <p>“The students have done a great job, they all have found it fun to see themselves included in the art,” she said.</p>
            
            <p>The base of the mural reads: “They didn't know we were seeds.” That is a partial quote from a Greek poet. There will also be a dedication to the Class of 2029.</p>
            
            <p>“We are all seeds, growing and turning into people with beautiful gifts and abilities,” King said.</p>
            
            <p>The goal of the project is to allow for a creative outlet. From the start it has been a youth-led endeavor, with the kids having input during the design phase and being the ones to put brush to wall, bringing the art to life.</p>
            
            <p>Gresham Outdoor Public Art, a nonprofit organization focused on bringing more publicly-accessible art to all corners of the city, funded the project with a $6,000 donation. That group also bought and donated several classroom copies of the inspirational book that spurred all of this.</p>
            
