          <p><strong>Glass sculptures in downtown come courtesy of Mt. Hood Community College student artists</strong></p>

          <p>New art is gracing downtown Gresham thanks to a collaboration between a Portland artist and 15 students from Mt. Hood Community College.</p>

          <p>â€œImagine" was created by artist Jen Fuller, a former educator at MHCC, and students from the college's Visual Arts department. Located at the corner of Northeast Hood Avenue and Northeast Third Street, the project was supported by the city of Gresham and Gresham Outdoor Public Art.</p>

          <img src="img/imagine/glassarticle2.jpg" alt="Glass sculptures" class="center"/>
          Outlook Photo: Christopher Keizur - The city's newest public art, Imagine, can be found at the corner of Northeast Hood Avenue and Northeast Third Street.
          <br>
          <br>

          <p>"This was one of the nicest projects I have ever worked on," said Fuller, a glass-and-steel installation artist based out of Portland.</p>

          <p>"Imagine" is the first piece of public art Fuller has installed in Gresham. She celebrated the dedication alongside her fellow artists and members of the community on Wednesday evening, May 30.</p>

          <p>The art has three columns of stained glass sharing the phrase "Imagine the Possibility," with panels depicting scenes created by the students who helped put the piece together. The result is an eclectic combination of artistic visions.</p>

          <p>"Each one of the students have their own style, so we looked for ways to express their individuality through the work," Fuller said.</p>

          <p>The students who helped complete the piece were Chelsea Halpin, James Edwards, Shannon Clare, Colton South, Micah Whalen, Samuel Turner, Kate Vincent, Molly Garnette Anderson, Meg Turner, Emily Miller, Jade Souza, Jean-Louis Moch Van Mol, Rafael Ordorica-Rea, Julie Chura and Carl Grassl.</p>

          <img src="img/imagine/glassarticle3.jpg" alt="Glass sculptures" class="center"/>
          Outlook Photo: Christopher Keizur - Portland-based artist Jen Fuller coordinated the art, which features ideas from the MHCC students who helped put it together.
          <br>
          <br>

          <p>Many of the MHCC artists had never worked with glass before, and loved being able to explore the new medium. They were chosen by merit to help complete the year-long process of bringing the art into downtown.</p>

          <p>Because of the complications of completing the piece, and last-minute adjustments, they realized "Possibility" was misspelled on the final panel. That part of the piece will be fixed soon.</p>

