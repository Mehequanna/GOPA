          <h2>New bronze otter unveiled at Nadaka Nature Park after 2024 theft</h2>
          <h3>By Christopher Keizur, The Outlook</h3>
          <br>
          <img src="img/nadaka/nadaka251.webp" alt="Group of people sitting around bronze otter statue." class="center"/>
          <p>The community celebrated the unveiling of a new bronze otter sculpture in Nadaka Nature Park after the original was stolen in 2024.</p>
          <br>
          <img src="img/nadaka/nadaka252.webp" alt="The bronze otter wearing wreaths." class="center"/>
          <p>"Slider Jr.," a new bronze sculpture, was unveiled at Nadaka Nature Park after the original was stolen.</p>
          <br>
          <img src="img/nadaka/nadaka253.webp" alt="Two women standing in Nadaka Nature Park." class="center"/>
          <p>Judy Han and Lee Dayfield.</p>
          <br>
          <img src="img/nadaka/nadaka254.webp" alt="Bronze raven statue on top of a pole." class="center"/>
          <p>"Seeker" a bronze raven statue at Nadaka Nature Park</p>
          <br>
          <img src="img/nadaka/nadaka255.webp" alt="Bronze otter statue at Nadaka Nature Park." class="center"/>
          <p>The bronze otter sculpture is back at Nadaka Nature Park in Gresham.</p>
          <br>
          <p>Last year unknown art poachers in Gresham caused vandalism and sorrow.</p>

          <p>In the middle of the night sometime before May 3, 2024, they cut at the base of a bronze sculpture of an otter, affectionately named “Slider” by local kids, in Nadaka Nature Park, 17615 NE Glisan St.</p>

          <p>That left “Seeker” the bronze raven alone in the park as police sought to find the missing otter. When nothing came of that investigation, a new solution was found.</p>

          <p>Gresham Outdoor Public Art, a nonprofit organization that installs works across the city, spurred the months-long effort to replace the statue. The organization contacted the city of Gresham Parks Department and gathered funds. They then contacted the original artist, Troutdale-based Rip Caswell, to bronze a nearly identical “Slider Jr.” The new otter statue was celebrated with an unveiling Tuesday morning, Nov. 25, at Nadaka Nature Park.</p>

          <p>“Get a few women together and things tend to get done,” said Judy Han, GOPA.</p>

          <p>A lot was done to make future theft effectively impossible. Before the otter statue was shallowly bolted onto a slab of concrete. That allowed the thieves to saw at those bolts, ultimately stealing the art.</p>

          <p>This time around the city dug down to create a solid pillar of concrete. More bolts were then submerged down to secure the statue in place. The city also cleared some of the brush and invasive species to improve sightlines throughout the park.</p>

          <p>So the new Slider shouldn't be swimming off anytime soon.</p>

          <h2>Creating Nadaka</h2>
          <p>It is fitting both iterations of Slider were powered by community volunteers and funding. That is how Nadaka Nature Park came to be in the first place.</p>

          <p>“This park has always existed because of community champions,” said Gresham City Councilor Cathy Keathley. “Thank you for championing this place and not letting it go away.”</p>

          <p>The original statue was donated by Joan Albertson, a longtime community member who normally prefers to remain in the background of projects. She again supported the replacement. The bronze otter was made by Rip Caswell, and the raven by his son Chad Caswell. They were first placed in the park in 2017.</p>

          <p>The park property in the Wilkes East Neighborhood was used by the Camp Fire Girls for outings and activities from 1956 to 1995 — Councilor Keathley was one of those youngsters who attended day camps at the site.</p>

          <p>Then it was sold to the city of Gresham, which installed a quarter-mile walking loop. But an eight-foot-tall, barbed wire-topped chain link fence deterred most from enjoying the space. So it effectively was a vacant property for years, with uncertainty swirling around its future.</p>

          <p>But then Lee Dayfield and her cohort of park advocates stepped in. Dayfield, a community advocate and nature lover, envisioned something more for Nadaka.</p>

          <p>“I could imagine what might happen here,” she said.</p>

          <p>Dayfield formed Friends of Nadaka — a coalition of public and private partners. That group engineered the purchase of two acres of private land, with support from a Metro Regional Government grant, then combined it for a 12-acre parcel.</p>

          <p>Grants continued to be secured to beautify the space and make it feel welcoming. The parcel was then conveyed to the city.</p>

          <p>Thus Nadaka Nature Park had its official grand opening in April 2015. The park now has a loop trail through the woods; a nature-based play area for kids (where the statues are located); a community garden; restroom and picnic shelter; and Americans with Disabilities Act accessible walking paths.</p>

          <p>“Today is what makes Nadaka Nature Park so special — it is community, creativity, and dedication to nurturing this space for generations to come,” Keathley said. “Nadaka is a shining example of what we can accomplish together.”</p>

