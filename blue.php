          <h2 class="text-center">Artist: Heather Greene</h2>
          <p class="text-center">Heather Greene was curious from early on and had a fascination with her father working in a bronze foundry. At a year old her father gave her some soft wax and hours later she had created a sculpture of 5 variations of the female form. By age 2 she sculpted over 100 pieces which her father cast in bronze. At the age of 3 she sold 30 pieces at her first art show and was hired to sculpt her first commission.</p>

          <p class="text-center">Heather gained national and worldwide attention as a young sculpting prodigy when her story was featured by Paul Harvey, People magazine, National Geographic world, and “That’s Incredible”. Heather Moved to Portland to work in a bronze foundry as a welder. In 2009 she bought the foundry she worked at and moved it to the heart of the Columbia River Gorge.</p>

          <p class="text-center">Heather and her husband Richard Greene work together in their studio creating monuments and timeless bronze statues of collectors and institutions worldwide. She is currently sculpting the world’s largest bronze Bald Eagle. It is sculpted entirely by hand without the use of enlargement technology that is so popular with artists right now.</p>

