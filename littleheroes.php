            <h2>Badges in Bronze -<br> Gresham statues to honor first responders
                <br>by Christopher Keizur for Gresham Outlook</h2>
            <br>
            <p><strong>Trio of sculptures to celebrate Gresham Police, Fire being crafted by Jeremy Pelletier</strong></p>

            <img src="img/littleheroes/badgesinbronze1.jpg" alt="Boy with firehouse hat drinking from hose." class="center"/>
            PMG PHOTO: CHRISTOPHER KEIZUR - A maquette model of one of three bronze statues that will be displayed in honor of Gresham Fire and Police.<br><br>

            <p>
              Growing up every kid dreams of their future job — soaring into space as an astronaut, wrangling cattle as a cowboy, saving lives as a doctor, discovering lost cultures as an archeologist, or battling infernos as a firefighter.<br><br>
              That whimsical passion and imagination of kids sparked the inspiration behind a new series of bronze sculptures being crafted for Gresham, which not only immortalize the potential of local youths, but also celebrate first responders across the city.<br><br>
              "This all started on a whim, and has morphed into a really exciting art project," said David Baumann, a member of Gresham Outdoor Public Art.<br><br>
              About five years ago Baumann was driving past Gresham City Hall when his wife, Kendra, noted the lack of public artwork in the complex, which includes the flagship Police Department, Fire Station and Gresham-Barlow School District offices.<br><br>
              "She mentioned how nice it would be to have some artwork on display," Baumann said. "That is when I thought of Jeremy's work."
            </p>

            <img src="img/littleheroes/badgesinbronze2.jpg" alt="Jeremy hold statue of boy drinking from firehose with firehouse helmet on." class="center"/>
            PMG PHOTO: CHRISTOPHER KEIZUR - Jeremy Pelletier is crafting a trio of bronze sculptures that will be displayed in front of Gresham City Hall.<br><br>

            <p>
              Jeremy Pelletier is an East Multnomah County artist who had a series of sculptures the couple had run across about a decade earlier. Called "Little Heroes," they were all small, playful statues of kids dressed up in different careers — firefighter, doctor, police officer.<br><br>
              "Every kid has a dream of what they want to be growing up," Baumann said. "We wanted this latest project to be cute and humorous."<br><br>
              Pelletier is now diligently working on Gresham's latest public art — a trio of bronze statues which will be unveiled outside of Gresham City Hall next spring. There is a little boy, dressed as a firefighter, bent over to drink from a running hose; a young girl in fire overalls with a badge on her shirt; and Officer Tagg, the department's comfort dog, who is sitting beside them in his vest and a toy in mouth.<br><br>
            </p>

            <img src="img/littleheroes/badgesinbronze3.jpg" alt="3D rendering of Little Heroes statue" class="center"/>
            COURTESY PHOTO: JEREMY PELLETIER - The digital rendering of what all three statues in honor of first responders will look like, with a planned installation of next spring.<br><br>

            <p>
              "It is really exciting to be doing these pieces, I like the idea of having my stuff out where a lot of people can see and enjoy it," Pelletier said.<br><br>
              The statues are being funded by GOPA, which has spurred many other public art across Gresham from the bronze sculptures in downtown to the murals at the Rockwood Market Hall.<br><br>
              "We wanted to support both the police and fire departments with this piece," Baumann said. "They are silent heroes in our community, so this is something for them."
            </p>

            <img src="img/littleheroes/badgesinbronze4.jpg" alt="Jeremy working in front of two monitors" class="center"/>
            PMG PHOTO: CHRISTOPHER KEIZUR - As an artist, Jeremy Pelletier begins his process crafting digital clay on the computer.<br><br>

            <p>
              <strong>Screen to reality</strong><br><br>
              Pelletier utilizes futuristic tech to craft his artwork.<br><br>
              The beginning stages of design all take place on a screen, using a program that allows him to pull and manipulate a digital ball of clay. Those pixels eventually morph into a physical, 3D-printed maquette, or model, that is later scaled-up at the foundry.<br><br>
              "I will jump straight from an idea in my head to sculpting on the screen," Pelletier said. "This tool is really powerful, because the program is deep and there is a lot you can do."<br><br>
              Out of high school Pelletier attended art school with a focus on drawing and painting. But during an art history course, which tasked him to dive into a new medium, he began working on an ancient Greek-style bust.<br><br>
              "I immediately fell in love with sculpting," he said. "I like working things with my hands, and being creative in all mediums."<br><br>
              From that point he has always been involved in creating. He has freelanced for companies and foundries in the past, and is currently working in the video game industry as the Art Manager for a company that completes outsourced 3D models.<br><br>
              He uses those same systems for the trio of sculptures, which he has been working on since the spring. The young boy in the fire hat was originally part of his "Little Heroes" series, but the other two have been original creations for Gresham.<br><br>
              "My son posed for the firefighter," Pelletier said. "I like finding whimsy in my work."
            </p>

            <img src="img/littleheroes/badgesinbronze5.jpg" alt="A 3D printed model of the boy dressed as a firefighter" class="center"/>
            COURTESY PHOTO: JEREMY PELLETIER - An early 3D printed model of a young boy dressed as a firefighter, designed by Jeremy Pelletier and eventually cast at Firebird Foundry.<br><br>

            <p>
              The boy will eventually be 41 inches tall, while the girl will be taller as she stands up straight. Beside them will be the statue of Tagg, a four-year-old black Labrador who came to the Gresham Police Department by way of Guide Dogs for the Blind. Tagg is a comfort dog, arriving on scene to support any community members suffering trauma. He is especially beloved by kids.<br><br>
              Tagg makes appearances at dementia care facilities, classrooms, and community festivals. He first joined the department in early 2020, and one month into the job he visited students at Dexter McCarty Middle School the morning after a classmate was hit and killed walking to school. He also supports his two-legged coworkers, hanging out in the department in-between shifts.<br><br>
              "Taking photos of Tagg to use as references was one of the best parts of this process," Pelletier said.<br><br>
              One small detail that will be in the final piece — a pouch in the bronze vest filled with business cards that the real Tagg carries with him on the job.<br><br>
              These pieces are not only Pelletier's first work in Gresham, but also his first major public installation.<br><br>
              "I've never worked at this scale before," he said.<br><br>
              Over the next few months Pelletier will complete the digital carvings, which will all then be 3D printed. After some final tuning, those will be cast at Firebird Foundry in Troutdale by Chad Caswell. The unveiling is planned for next spring.<br><br>
              "We can't wait for the community to experience these statues," Baumann said with a smile.<br><br>
            </p>

