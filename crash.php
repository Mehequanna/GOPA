          <p class="text-center">I made the Sculpture my senior year of High School as a senior project. I had a very hard time figuring out what I wanted to make. I ran by several ideas to Mr. Etnier my teacher before finally coming up with this idea. It was actually his idea I donate it to the city. I knew there was a new skate park in the works and I thought it would be a good place for it. I explained idea and they went for it. Mr. Etnier was a huge help on this project. It started as a piece of construction paper where I traced the outline of my friend. I then traced the outline onto a 3/4 inch piece of steel and cut out the body, head, and skateboard. I used cylinder shaped steel as wheels. Mr. Etnier came up with the idea to heat the medal and bend his arms to make it appear less two dimensional. I did many welds on each piece to ensure the welds were strong. It took me several months to finish this project. I had to come in on my spare time to complete it. Skateboarding was always a huge part of my life when I was younger as well as art. I always thought art was interesting, especially pieces around the city. I spent a majority of my high school years skateboarding, wishing there were more skate parks in the area to enjoy (getting kicked out of places around the city was never fun). It's cool to see some new parks. I have yet to see my sculpture up in person. I have only seen it as a picture. I am station in Fort Leonard Wood, MO and have not been back to Oregon for a long time. I think kids should be more involved in art and create things that can be displayed around the city. It's a good feeling knowing something you created is up for display somewhere.
          <br>
