          <h2>Art installation brings books back to Carnegie Library
            <br>by Christopher Keizur for Gresham Outlook</h2>
          <br>
          <p><strong>'Little Library' unveiled, designed after iconic Gresham History Museum in downtown</strong></p>

          <p>When Mark Klobas was 10 years old, he saw the film "My Side of the Mountain" in the theater, and the story sparked a passion for the outdoors.</p>

          <p>The protagonist of the film left his home in New York City to live in the wilderness — crafting his own shelter, fishing, hiking, training a falcon and coping with a life dependent on nature. That lifestyle appealed to Klobas, who was living in Troutdale. He decided to learn all he could about the outdoors so he too, would be able to start a new life in the woods.</p>

          <p>Nearly every afternoon, Klobas would ride his bicycle into downtown Gresham to visit the Carnegie Library — which is now the Gresham History Museum at 410 N. Main Ave. He read about edible plants, local wilderness areas, and found an article about falconry in an old magazine in the reference section of the library.</p>

          <p>"I would go in there and read that article about falconry all the time," Klobas said with a laugh. "The library was really important to me with these dreams of living in the wilderness."</p>

          <img src="img/littlelibrary/open-library.jpg" alt="Woman looking at open little library." class="center"/>
          PMG Photo: Christopher Keizur - The Little Library was designed as a miniature replica of the historic Carnegie Library, now home to the Gresham History Museum at 410 N. Main Ave.
          <br>
          <br>

          <p>So when Klobas was asked earlier this year to create a functional piece of art that would not only celebrate the Carnegie Library, but also bring back book lending to the historic Gresham building, he jumped at the chance.</p>

          <p>"I didn't even have to think about it. This is about people coming together and reading," Klobas said.</p>

          <p>Klobas constructed a "Little Lending Library" that was unveiled Thursday evening, June 17, next to the Gresham History Museum. The art piece, commissioned by Gresham Outdoor Public Art, looks just like the famous Tudor-style building it sits alongside, down to the minute details of brick siding.</p>

          <p>"This was something new and different to make, it was a fun challenge," Klobas said.</p>

          <p>The latest public art piece in Gresham is also functional. Anyone is welcome to pick up or leave a book at the lending library, which has something to read for all ages.</p>

          <img src="img/littlelibrary/mark-library.jpg" alt="Creator Mark Klobas standing next to little library." class="center"/>
          PMG Photo: Christopher Keizur - Mark Klobas, who created the Little Library art piece, would visit the former Gresham Carnegie Library to read about the woods.
          <br>
          <br>

          <p>There are already hundreds of books waiting to restock the shelves, as a group of youths came together to gather donations from their peers. Gresham's Luke Bemis and his friends collected nearly 300 books for school aged kids to enjoy.</p>

          <p>"I told them they are now stewards of the library," said Judy Han with GOPA. "What I love about these little free libraries is the whole community supports them."</p>

          <p>For Klobas, he hopes the "Little Library" helps inspire community members to discover their own passions. Much like when he was a 10-year-old boy voraciously reading all about the wilderness</p>

          <p>"This was a work of love," he said.</p>

          <p>Klobas may even leave a book on falconry for some child to find.</p>

