            <h2>Group of Gresham educators, parents finish garden classroom
                <br>by Angel Rosas for Gresham Outlook</h2>
            <br>
            <p><strong>After years of work, the Hollydale Elementary School garden nears completion</strong></p>

            <img src="img/hollydale/gardenarticle1.jpg" alt="Students and teacher look at plant in garden" class="center"/>
            COURTESY PHOTO: GRESHAM-BARLOW SCHOOL DISTRICT - Hollydale Elementary School students got to interact with the garden during the summer months.
            <br>
            <br>

            <p>What was once a small forgotten space filled with overgrown weeds in the corner of Hollydale Elementary School, is now a nearly completed garden classroom that will be used to educate generations of students.</p>

            <p>The garden started back in 2002 but fell into disrepair after the principal who started the garden left the school. With no supervision, the small plot of garden beds quickly fell into disrepair.</p>

            <p>In 2016, then Gresham-Barlow School District teacher Julie Frediani saw what the space could be and decided to make it her mission to build a garden that could be used to teach students about nature, agriculture and so many other topics.</p>

            <p>"I had a lot of different parents come to these work parties where we pulled weeds and made paths," Frediani said. "It was a kid and parent powered project."</p>

            <p>So, Frediani applied to the school's Parent Teacher Club to help restore the garden and received about $10,000 to do it. With those funds, Frediani, her family bought a new shed, benches and supplies to build new garden beds. The garden team also had to dismantle the previous beds and an old greenhouse that was unusable.</p>

            <p>Now working for the district as an education assistant and substitute teacher, Frediani is still determined to complete the school's garden sanctuary.</p>

            <img src="img/hollydale/gardenarticle2.jpg" alt="Group of students talking about what they learned." class="center"/>
            COURTESY PHOTO: GREHSHAM-BARLOW SCHOOL DISTRICT - Hollydale students learned about butterflies during a visit to the school's garden.
            <br>
            <br>

            <p>"I could not let (the garden) go," Frediani said. "I told myself that I would make this garden into what we had envisioned even if I am retired."</p>

            <p>As more and more work went into the garden, Frediani started to develop an even stronger base of volunteers and school staff who shared her vision and dedication. Frediani was able to recruit fellow educating assistants Gaila Crawford and Julie Melton who have been instrumental in advancing the garden project.</p>

            <p>The garden has also gotten some great donations like solar panel generators. However, to make the garden what she had envisioned, Frediani felt that there needed to be a space within the area where students could be covered from the sun and the rain.</p>

            <p>So, the team thought a gazebo would be a perfect final piece to the garden classroom. Frediani applied for the Gresham's Community Enhancement Grant, but unfortunately due to a communication error were not awarded the funds. Frediani, ever persistent to finish the garden, decided to apply for a grant offered by the Gresham Outdoor Public Art (GOPA). The garden's application was accepted, and the garden project received $4,750 to pay for the gazebo.</p>

            <p>"We want children to come out here to read, draw and just enjoy the space even when it is hot or drizzly," Frediani said. "Or they can just come to enjoy the peace."</p>

            <p>As of now, the garden has been utilized by many of the teachers to help their students learn about where their food comes from, how certain plants grow and even just a place where students can relax.</p>

            <p>"Kids need spaces out of a classroom," Frediani said. "They need to have places where they can be calm, deescalate and feel like they have control." Frediani, Melton and Crawford said that they have each used the garden for dozens of different lessons and feel like the space allows them to engage in topics in a different way.</p>

            <p>The garden has several beds that are filled with various vegetable and flower plants.</p>

            <img src="img/hollydale/gardenarticle3.jpg" alt="Student digging for plany in flower bed." class="center"/>
            COURTESY PHOTO: GRESHAM-BARLOW SCHOOL DISTRICT - Nearly all of the plants growing at the Hollydale garden have been planted by students.
            <br>
            <br>

            <p>"Many of the kids that I have interacted with don't know where their food comes from," Melton said.</p>

            <p>As the garden nears completion, Frediani hopes that dedicated teachers, staff and parents continue to put the work to keep the garden functioning and continue to use it as a tool to educate students well into the future.</p>

