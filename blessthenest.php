          <p><strong>Latest sculpture from Heather Soderberg unveiled during Gresham Arts Festival</strong></p>

          <p>A family of ducks has moved into downtown Gresham.
          The latest bronze sculpture was unveiled Friday evening, July 19, during the Art Under the Stars, which officially kicks off the Gresham Arts Festival.
          The unveiling of "Bless Our Nest," presented and funded by the Gresham Area Chamber of Commerce, is a piece featuring a family of seven wood ducks — a mother, father and five baby ducklings. There is plenty to discover on the statue, as there is also a painted turtle, spotted frog and many other creatures.</p>

          <img src="img/blessthenest/nestarticle2.jpg" alt="Bless the Nest statue" class="center"/>
          Outlook Photo: Christopher Keizur
          <br>
          <br>

          <p>The statue, which was also brought to the community thanks to the efforts of Gresham Outdoor Public Art, is located at the corner of Northwest Third Avenue and North Main Avenue, in front of Café Delirium.
          "This intersection now symbolizes all of Gresham with these amazing pieces," said Lynn Snodgrass, CEO of the Gresham Chamber. "We have now completed this intersection with all the art."</p>

          <p>The statue was made by Heather Soderberg, a Cascade Locks-based artist, who attended the unveiling. Soderberg already has several statues in place across downtown Gresham. She made "Driscoll" the bronze guide dog, "Blue" the bronze heron, and the "Mister Gresham" statue of Todd Kirnan, all of which are located at the same intersection.</p>

          <img src="img/blessthenest/nestarticle3.jpg" alt="Lynn looking at Bless the Nest statue" class="center"/>
          Outlook Photo: Christopher Keizur
          <br>
          <br>

          <p>There is also a piece by Troutdale artist Chad Caswell — "Teddy," a bronze bear on a bench that honors the annual Gresham Teddy Bear Parade.
          "Art offers value to our community," Snodgrass said.
          In addition to the unveiling, there was plenty of fun to be had during Art Under the Stars. A free concert was put on by the Norman "Boogie Cat" Sylvester Band, there was a beer and wine garden, and lots of local cuisine.
          The festivities also supported public artwork in Gresham, with a silent auction featuring pieces from some of the premier artists who headlined the Gresham Arts Festival.</p>

